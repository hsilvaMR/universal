				<br>1.2	Reserva sujeita à disponibilidade do Hotel.
				<br>1.3	O premiado é responsável por selecionar o Hotel e fazer a respetiva reserva.
				<br>1.4	O prémio terá de ser usufruído até 31 de dezembro de 2020.
				<br>1.5	O prémio não pode sob qualquer circunstância, ser trocado por valor monetário ou por outros packs.

