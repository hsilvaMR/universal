	            <h2>Regulamento do passatempo “Pergunta para Queijinho” - Universal de 2019</h2>
	            <br><br>
				<p><b>I - Introdução</b> 
				<br><br>1. O Passatempo "Pergunta para Queijinho” é levado a cabo pela Universal (LA – produtos lácteos unipessoal, Lda.) na sua página de Facebook (<a href="https://www.facebook.com/queijouniversal" target="_blank" style="color:#0B477C;">https://www.facebook.com/queijouniversal</a>) e na sua página do Instagram (<a href="https://www.instagram.com/universal_queijo" target="_blank" style="color:#0B477C;">https://www.instagram.com/universal_queijo</a>), com o objetivo de premiar os fãs e consumidores da marca Universal. 


				<br>2. O modo de participação no passatempo, bem como a forma de atribuição e entrega dos prémios são regulados pelo presente Regulamento.</p>
	
				<p><br><br><b>II - O Passatempo</b>
				<br><br>1. Entre os dias 22 de fevereiro de 2019 e 31 de dezembro de 2019, quem quiser participar deve fazê-lo através da página do Facebook da Universal ou através da página do Instagram da Universal. 

				<br>2. Quinzenalmente, às sextas-feiras, será lançada uma pergunta. Ocasionalmente poderão ser lançadas perguntas em outros dias, é por isso importante estar atento às redes sociais da Universal.

				<br>3. No decorrer do passatempo, serão feitas 24 perguntas.

				<br>4. Para efetuar a sua participação no passatempo deve responder acertadamente ao maior número possível de “Perguntas para Queijinho”.</p>

				<p><br><br><b>III  -  Prémios</b>
				<br><br>1. No âmbito do presente passatempo serão atribuídos os seguintes prémios:

				<br><br><b>a) Ao primeiro classificado:  2 queijos</b> (1 queijo bola Universal e 1 queijo prato Universal);
				<br><br><b>b) Ao segundo classificado:  1 queijo</b> (1 queijo bola Universal);
				<br><br><b>c) Ao terceiro classificado:  1 queijo</b> (1 queijo bola Universal);
				<br><br><b>d) Ao quarto classificado:  1 queijo</b> (1 queijo mini bola Universal);
				<br><br><b>e) Ao quinto classificado:  1 queijo</b> (1 queijo mini bola Universal).

				</p>

				<p><br><br><b>IV  -  Divulgação</b>
				<br><br>1. A publicidade ao Passatempo será feita pela Universal nos seus canais online: Facebook, Instagram e/ou website.</p>

				<p><br><br><b>V - Requisitos dos participantes</b> 
				<br><br>1. Podem participar no Passatempo todas as pessoas com residência em Portugal Continental. 

				<br>2. Não poderão inscrever-se, concorrer nem participar no concurso os funcionários das empresas direta ou indiretamente envolvidas na organização deste concurso: funcionários, executivos, diretores e/ou consultores, bem como parentes destes, incluindo pais, irmãos, filhos e cônjuges (independentemente do seu domicílio), e as pessoas com as quais residam (independentemente do grau de parentesco).

				<br>3. Os participantes têm de ser maiores de idade.</p>


				<p><br><br><b>VI - Como participar</b> 
				<br><br>1. Na página oficial da Universal no Facebook (<a href="https://www.facebook.com/queijouniversal" target="_blank" style="color:#0B477C;">https://www.facebook.com/queijouniversal</a>) ou na página oficial da Universal no Instagram (<a href="https://www.instagram.com/universal_queijo" target="_blank" style="color:#0B477C;">https://www.instagram.com/universal_queijo</a>)</p>


				<p><br><br><b>VII  -  Seleção do Vencedor</b>
				<br><br>1. Para concorrer é necessário responder ao maior número de perguntas. Os que acertarem mais vezes às perguntas são os vencedores. 
				<br>2. No caso de empate, vencerá a pessoa que tiver sido mais rápida a responder ao maior número de perguntas. 
				<br>3. A Universal dará a resposta às perguntas, no espaço de uma semana, na zona dos comentários do respetivo post.  
				<br>4. As participações que ocorrerem após ter sido desvendada, pela Universal, a resposta à “Pergunta para Queijinho”, não serão consideradas válidas. 
				<br>5. Por cada pergunta só é válida uma participação por pessoa.
				<br>6. Ao participar através das duas redes sociais, será apenas contabilizada uma participação. 
				<br>7. Será atribuído 1 prémio por cada vencedor.
				<br>8. Os vencedores serão anunciados nas páginas do Facebook, Instagram e website da Universal no dia 10 de janeiro de 2020. 
				<br>9. Cada um dos vencedores receberá uma mensagem privada a informar que ganharam o prémio, onde serão também solicitados dados pessoais para envio dos respetivos prémios.
				<br>10. É da responsabilidade do vencedor responder à mensagem e enviar os dados solicitados.
				<br>11. Caso o vencedor não envie os dados solicitados até dia 20 de janeiro de 2020, o mesmo será atribuído a outro participante, seguindo a mecânica de participação estabelecida. Este processo repetir-se-á até que o prémio do presente passatempo seja efetivamente aceite.
				<br>12. A Universal não se responsabiliza pela impossibilidade de contacto com o vencedor nem por este apresentar dados incorretos. 
				<br>13. Os prémios não podem ser trocados nem convertidos em dinheiro.
				<br>14. Os prémios são pessoais e intransmissíveis.</p>

				<p><br><br><b>VIII  -  Entrega do Prémio</b> 
				<br><br>1. O prémio correspondente será enviado, através de transportadora, mediante o envio do contacto do premiado.</p>

				<p><br><br><b>X - Prémios especiais a atribuir</b>
				<br><br>Para além do cumprimento do descrito no restante regulamento.
				<br><br>1.	Quem:
				<br><br>&emsp;&emsp;a)	Responder acertadamente a, no mínimo, 14 <b>“Perguntas para Queijinho”</b>; 
				<br>&emsp;&emsp;b)	Identificar 2 amigos (por cada nova pergunta);
				<br>&emsp;&emsp;c)	Reunir, e nos enviar, 18 rótulos inteiros (de qualquer queijo da marca Universal); 
				habilita-se ainda a ganhar uma <b>“Viagem para 2 pessoas aos Açores”</b>.

				<br><br>2. Quem:
				<br><br>&emsp;&emsp;a)	Responder acertadamente a, no mínimo, 10 <b>“Perguntas para Queijinho”</b>;
				<br>&emsp;&emsp;b)	Reunir, e nos enviar, 6 rótulos inteiros (de qualquer queijo da marca Universal);
				habilita-se ainda a ganhar uma <b>“Estadia de 2 noites para 2 pessoas”</b>.

				<br><br>3. Ao cumprir os requisitos do ponto 1 está a cumprir simultaneamente os requisitos do ponto 2.
				<br>4. Será apenas entregue 1 prémio especial de cada. Ou seja, apenas uma <b>“Viagem para 2 pessoas aos Açores”</b> e apenas uma <b>“Estadia de 2 noites para 2 pessoas”</b>.

				<br>5. A <b>“Viagem para 2 pessoas aos Açores”</b> será entregue ao melhor classificado deste passatempo que simultaneamente cumprir com os requisitos para a atribuição deste prémio especial (tal qual ponto 1 do presente capítulo).

				<br>6. A <b>“Estadia de 2 noites para 2 pessoas”</b> será entregue ao melhor classificado deste passatempo que simultaneamente cumprir com os requisitos para a atribuição deste prémio especial (tal qual ponto 2 do presente capítulo), excluindo o vencedor da <b>“Viagem para 2 pessoas aos Açores”</b>.

				<br><br>7. Deve ainda ser tido em conta que:

				<br><br>&emsp;&emsp;a)	No que respeita aos pontos 1 e 2, quem tiver enviado mais rótulos não é necessariamente o vencedor, pois este é determinado pelo maior número de respostas certas à <b>“Pergunta para Queijinho”</b>, tal como referido no ponto 1 do capítulo VII do regulamento do concurso. 
				<br>&emsp;&emsp;b)		No caso de empate para o número de rótulos enviados, prevalece o maior número de respostas certas à <b>“Pergunta para Queijinho”</b> como fator de desempate, tal como referido no ponto 1 do capítulo VII do regulamento do concurso. 
				<br>&emsp;&emsp;c)		No que respeita ao ponto 1, quem tiver identificado mais amigos não é necessariamente o vencedor, pois este é determinado pelo maior número de respostas certas à <b>“Pergunta para Queijinho”</b>, tal como referido no ponto 1 do capítulo VII do regulamento do concurso.
				<br>&emsp;&emsp;d)		No caso de empate para o número de amigos identificados, prevalece o maior número de respostas certas à “Pergunta para Queijinho” como fator de desempate, tal como referido no ponto 1 do capítulo VII do regulamento do concurso.
				<br>&emsp;&emsp;e)		No caso de empate para o número de respostas certas à <b>“Pergunta para Queijinho”</b>, vencerá a pessoa que tiver sido mais rápida a responder ao maior número de perguntas, tal como referido no ponto 2 do capítulo VII do regulamento do concurso.

				<br><br>8. Os rótulos têm de ser enviados por correio, até dia 20 de dezembro de 2019, para:<br>
