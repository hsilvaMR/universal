				<br>1.5 O prémio é pessoal e intransmissível e não pode sob qualquer circunstância, ser trocado por valor monetário, cheques viagens ou vouchers.
				<br>1.6 Não são permitidas alterações de destino nem de dados (datas e local de partida).
				<br>1.7 Caso exista suplementos, despesas pessoais, noites extra, transfers ou qualquer outro tipo de despesa não incluída no prémio, deverão ser liquidados pelo vencedor antes do início da viagem. Caso contrário a Universal poderá cancelar a reserva e o respetivo prémio. 
				<br>1.8 O cancelamento da viagem por parte do premiado após confirmação, implica que este suporte todos os custos tidos até ao momento. Nesta situação, o premiado não poderá voltar a usufruir deste prémio em qualquer circunstância.
 				
